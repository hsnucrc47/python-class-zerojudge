''' 
內容：
大於 0、整數、不可以被 7 整除、小於 n，請輸出所有可能的數字。

輸入說明：
輸入為一個整數 n，其中 n 不大於 10000。
若 n = 0 表示資料結束。

輸出說明：
輸出如前述，各個數字之間以一個空白隔開。
'''

a = ""  # 設a為答案
while True: 
    n = int(input())
    if n != 0: # 若 n = 0 則結束
        for i in range(1, n):
            if i % 7 == 0: # 如果被 7 整除就跳過
                continue
            a += str(i) # 把數字放進答案
            a += " " # 以一個空白隔開
        a += "\n" #換行
    else:
        print(a)
        break
